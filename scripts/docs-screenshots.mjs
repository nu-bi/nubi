// ── Manifest: stamp the run with the project version + commit ────────────────
const pkg = JSON.parse(readFileSync(path.join(ROOT, 'package.json'), 'utf8'))
let commit = 'unknown'
try {
  commit = execSync('git rev-parse --short HEAD', { cwd: ROOT }).toString().trim()
} catch { /* not a git checkout (e.g. tarball) — manifest still useful */ }
writeFileSync(
  path.join(OUT_DIR, 'manifest.json'),
  JSON.stringify(
    {
      version: pkg.version,
      commit,
      generatedAt: new Date().toISOString(),
      files: [...captured].sort().map((n) => `${n}.png`),
    },
    null,
    2
  ) + '\n'
)
console.log(`✓ manifest.json (version ${pkg.version}, commit ${commit})`)

// ── Stale-reference check ─────────────────────────────────────────────────────
// Every screenshot referenced from README.md or docs/*.md must have been
// regenerated by this run; otherwise the docs point at images that will rot.
const mdFiles = [
  path.join(ROOT, 'README.md'),
  ...readdirSync(path.join(ROOT, 'docs'))
    .filter((f) => f.endsWith('.md'))
    .map((f) => path.join(ROOT, 'docs', f)),
]
const refs = new Set()
for (const f of mdFiles) {
  const text = readFileSync(f, 'utf8')
  for (const m of text.matchAll(/(?:\/docs\/screenshots\/|docs\/assets\/)([\w][\w.-]*\.png)/g)) {
    refs.add(m[1])
  }
}
refs.delete('logo.png') // static brand asset, not a product capture
const missing = [...refs].filter((n) => !captured.includes(n.replace(/\.png$/, '')))
if (missing.length) {
  console.error(`\n✗ Referenced in README/docs but NOT captured this run: ${missing.join(', ')}`)
  console.error('  Add captures for these (or remove the stale references).')
  process.exitCode = 1
} else {
  console.log(`\nDone. ${captured.length} screenshots; all ${refs.size} doc-referenced images regenerated.`)
}
console.log(`Screenshots in ${OUT_DIR} and ${ASSETS_DIR}`)
