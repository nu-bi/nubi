/**
 * docs-screenshots.mjs — regenerate every product screenshot embedded in the
 * docs AND the README hero images, stamped with the current project version.
 *
 *   npm run screenshots        # against an already-running dev stack
 *   npm run screenshots:auto   # boots an ephemeral seeded stack first
 *
 * Requirements (manual mode):
 *   - Dev stack running: vite on :5173, API on :8000.
 *   - Seeded admin account (admin@nubi.dev) with the Demo project
 *     (10 dashboards, 38 queries, demo connector).
 *
 * Output:
 *   - public/docs/screenshots/<name>.png — referenced from docs markdown as
 *     /docs/screenshots/<name>.png. Viewport 1440x900 @2x, light theme.
 *   - docs/assets/hero-{light,dark}.png — the README hero (full-viewport
 *     dashboard view), one per color scheme.
 *   - public/docs/screenshots/manifest.json — version + commit + file list
 *     for the run, so stale screenshots are detectable.
 *
 * Exit code 1 if any screenshot referenced from README.md or docs/*.md was
 * not regenerated by this run (i.e. docs reference an image we no longer
 * capture). Re-runnable: resource ids (boards, queries, flows, projects) are
 * discovered by name via the API on every run, so reseeding does not break it.
 */
