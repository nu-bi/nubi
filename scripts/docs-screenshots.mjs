// ── Discover seeded resource ids via the API ─────────────────────────────────
async function api(pathname, token, projectId) {
  const headers = { 'Content-Type': 'application/json' }
  if (token) headers.Authorization = `Bearer ${token}`
  if (projectId) headers['X-Project-Id'] = projectId
  const res = await fetch(`${API}/api/v1${pathname}`, { headers })
  if (!res.ok) throw new Error(`GET ${pathname} → ${res.status}`)
  return res.json()
}

async function apiPost(pathname, token, projectId, body) {
  const headers = { 'Content-Type': 'application/json' }
  if (token) headers.Authorization = `Bearer ${token}`
  if (projectId) headers['X-Project-Id'] = projectId
  const res = await fetch(`${API}/api/v1${pathname}`, {
    method: 'POST',
    headers,
    body: JSON.stringify(body),
  })
  if (!res.ok) throw new Error(`POST ${pathname} → ${res.status}: ${await res.text()}`)
  return res.json()
}

// The flows screenshots need a multi-task DAG. Fresh seeds don't include one,
