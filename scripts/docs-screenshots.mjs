async function discover() {
  const loginRes = await fetch(`${API}/api/v1/auth/login`, {
    method: 'POST',
    headers: { 'Content-Type': 'application/json' },
    body: JSON.stringify({ email: EMAIL, password: PASSWORD }),
  })
  if (!loginRes.ok) throw new Error(`API login failed: ${loginRes.status}`)
  const { access_token: token } = await loginRes.json()

  const { orgs } = await api('/orgs', token)
  const org = orgs[0]
  const projects = await api(`/projects?org_id=${org.id}`, token)
  const demo = projects.find((p) => p.slug === 'demo' || p.name === 'Demo') ?? projects[0]
  const dflt = projects.find((p) => p.slug === 'default' || p.name === 'Default') ?? projects[0]

  const boards = await api('/boards', token, demo.id)
  const board =
    boards.find((b) => b.name === 'Retail Sales Overview') ?? boards[0]

  const queries = await api('/queries', token, demo.id)
  const queryList = Array.isArray(queries) ? queries : queries.queries ?? []
  const query =
    queryList.find((q) => /Retail — region × month/.test(q.name ?? '')) ??
    queryList.find((q) => /Retail/.test(q.name ?? '')) ??
    queryList[0]

  const flows = await api('/flows', token, demo.id)
  let flow =
    flows.find((f) => f.name === 'Retail revenue pipeline') ??
    flows.find((f) => (f.spec?.tasks?.length ?? 0) > 1) ??
    flows[0]
  if (!flow) {
    console.log('No flow in Demo project — creating "Retail revenue pipeline"…')
    flow = await ensureDemoFlow(token, demo.id)
  }

  return { org, demo, dflt, board, query, flow }
}

// ── Browser helpers ──────────────────────────────────────────────────────────
async function uiLogin(page, email = EMAIL, password = PASSWORD) {
  // Log in via the API through the app origin (Vite proxies /api), so the
  // HttpOnly refresh cookie lands straight in the browser's cookie jar.
  // Driving the login form flakes under rapid successive logins (hydration
  // and refresh-rotation races); this path has none of that.
  for (let attempt = 1; ; attempt++) {
    // Drop any stale refresh cookies first: the jar can hold one from an
    // earlier (rotated-out) session, and if the browser sends that one the
    // boot refresh 401s — or worse, trips family-reuse revocation.
    await page.context().clearCookies()
    const res = await page.context().request.post(`${APP}/api/v1/auth/login`, {
      data: { email, password },
    })
    if (res.ok()) {
      await page.goto(`${APP}/home`, { waitUntil: 'networkidle', timeout: 45_000 })
      await sleep(800)
      if (!page.url().includes('/login')) return
    }
    if (attempt >= 3) {
      throw new Error(`login failed for ${email}: ${res.status()} (url ${page.url()})`)
    }
    console.log(`  login attempt ${attempt} failed — retrying`)
    await sleep(1500)
  }
}

// Project/env live in localStorage and are read at app boot — switching them
// requires one full reload, which `ensureApp` performs when this flag is set.
let needsReload = true

async function setProject(page, orgId, projectId) {
  await page.evaluate(
    ([k, v]) => localStorage.setItem(k, v),
    [`nubi-active-project-id:${orgId}`, projectId]
  )
  needsReload = true
}

async function setEnv(page, projectId, envKey) {
  await page.evaluate(
    ([k, v]) => localStorage.setItem(k, v),
    [`nubi.activeEnv.${projectId}`, envKey]
  )
  needsReload = true
}

async function shoot(page, name, { settle = 1200, dir = OUT_DIR } = {}) {
  await sleep(settle)
  // Reset both window scroll and any internally scrolled pane (autofocus on
  // forms below the fold scrolls split-screen layouts).
  await page.evaluate(() => {
    window.scrollTo(0, 0)
    for (const el of document.querySelectorAll('*')) {
      if (el.scrollTop > 0) el.scrollTop = 0
    }
  })
  await sleep(150)
  await page.screenshot({ path: path.join(dir, `${name}.png`) })
  captured.push(name)
  console.log(`✓ ${name}.png`)
}

// Every full page load reboots the app, whose boot-time POST /auth/refresh
// rotates the refresh cookie. Two rapid reloads can present the same cookie
// twice, which the backend treats as token reuse and revokes the whole
// session family (auth/sessions.py) — bouncing us to /login. So: navigate
// client-side (SPA route change, no reboot) whenever possible, and do a real
// reload only when a project/env switch requires re-reading localStorage.
// If we do get bounced, re-login and retry.
let desiredProject = null
let desiredEnv = null
let orgId = null
async function recoverLogin(page) {
  await uiLogin(page)
  if (orgId && desiredProject) {
    await setProject(page, orgId, desiredProject)
    if (desiredEnv) await setEnv(page, desiredProject, desiredEnv)
  }
}

async function ensureApp(page, url) {
  for (let attempt = 1; attempt <= 4; attempt++) {
    const inApp = page.url().startsWith(APP) && !page.url().includes('/login')
    if (!inApp || needsReload) {
      // Full reload — but always land on /home, never deep-link the target:
      // a cold deep-link mounts the page before the boot-time auth refresh
      // has a token, so list fetches 401 and silently render empty states.
      await page.goto(`${APP}/home`, { waitUntil: 'networkidle', timeout: 45_000 })
      needsReload = false
      await sleep(800)
      if (page.url().includes('/login')) {
        console.log(`  bounced to /login booting /home — recovering (attempt ${attempt})`)
        await recoverLogin(page)
        continue
      }
    }
    // Client-side route change: no app reboot, so no refresh-token rotation
    // (rapid rotations trip the backend's reuse detection and revoke the
    // whole session family) and auth is already in memory.
    await page.evaluate((u) => {
      window.history.pushState({}, '', u)
      window.dispatchEvent(new PopStateEvent('popstate'))
    }, url)
    await page.waitForLoadState('networkidle', { timeout: 45_000 }).catch(() => {})
    await sleep(600)
    if (!page.url().includes('/login')) return
    console.log(`  bounced to /login on ${url} — recovering (attempt ${attempt})`)
    await recoverLogin(page)
  }
  throw new Error(`Could not load ${url} — kept bouncing to /login`)
}

async function goAndShoot(page, url, name, opts = {}) {
  await ensureApp(page, url)
  await shoot(page, name, opts)
}

