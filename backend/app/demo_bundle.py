def _parquet_s3_uri(project_id: str, dataset: str, table: str, bucket: str) -> str:
    """Return the canonical S3 URI for a demo table parquet file."""
    return f"s3://{bucket}/projects/{project_id}/demo/{dataset}/{table}.parquet"


def export_demo_to_s3(
    project_id: str,
    bucket: str | None = None,
    force: bool = False,
) -> dict[str, str]:
    """Export all four demo datasets to per-project S3 parquet files.

    Uses DuckDB ``COPY (SELECT * FROM <arrow table>) TO 's3://...'`` with
    httpfs + S3 SECRET configured by ``setup_s3_httpfs``.

    Idempotent — skips tables whose parquet file already exists in S3 unless
    ``force=True``.  The skip check is a try/read: if the parquet is readable
    the table is considered present.  Each dataset is only *generated* when at
    least one of its tables actually needs writing.

    Returns ``{table: s3_uri}`` for every table written (skipped tables absent).
    """
    import duckdb  # noqa: PLC0415

    from app.connectors.duckdb_conn import setup_s3_httpfs  # noqa: PLC0415
    from seed_data.generators import build_dataset  # noqa: PLC0415

    bucket = bucket or _s3_bucket()

    write_conn = duckdb.connect(database=":memory:")
    setup_s3_httpfs(write_conn)

    written: dict[str, str] = {}
    try:
        for dataset, tables in DATASET_TABLES.items():
            built = None  # generated lazily, only when a table needs writing
            for table in tables:
                uri = _parquet_s3_uri(project_id, dataset, table, bucket)

                if not force:
                    # Idempotency check — try to read the first row.
                    try:
                        write_conn.execute(
                            f"SELECT * FROM read_parquet('{uri}') LIMIT 1"
                        ).fetchone()
                        continue  # already exists; skip
                    except Exception:
                        pass  # not present or unreadable — proceed with write

                if built is None:
                    built = build_dataset(dataset)

                write_conn.register("_demo_src", built[table])
                try:
                    write_conn.execute(
                        f"COPY (SELECT * FROM _demo_src) TO '{uri}' (FORMAT parquet)"
                    )
                    written[table] = uri
                finally:
                    try:
                        write_conn.unregister("_demo_src")
                    except Exception:
                        pass
    finally:
        write_conn.close()

    return written


