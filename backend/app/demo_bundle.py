def s3_datastore_config(
    project_id: str,
    bucket: str | None = None,
) -> dict[str, Any]:
    """``duckdb`` datastore config reading all 17 demo tables from S3 parquet.

    One ``CREATE VIEW`` per table over ``read_parquet(s3://...)`` so the normal
    connector → /query → /data-browser pipeline picks every table up.  The
    httpfs/S3 SECRET is configured automatically at query time when ``s3://``
    references are detected (``setup_s3_httpfs`` resolves the credentials from
    environment variables — no plaintext secrets in the datastore row).
    """
    bucket = bucket or _s3_bucket()

    view_statements: list[str] = []
    for dataset, tables in DATASET_TABLES.items():
        for table in tables:
            uri = _parquet_s3_uri(project_id, dataset, table, bucket)
            view_statements.append(
                f"CREATE OR REPLACE VIEW {table} AS SELECT * FROM read_parquet('{uri}')"
            )

    return {
        "connector_type": "duckdb",
        "database": ":memory:",
        "view_sql": ";\n".join(view_statements),
        "description": (
            "Per-project demo datasets (retail sales, SaaS metrics, web analytics, "
            "finance ops) backed by S3 parquet files."
        ),
        "demo_project_id": project_id,
        "demo_s3_bucket": bucket,
        "sample": True,
        # Internal: backs the demo dashboards/queries by id. The connectors list
        # surfaces the branded virtual "Demo data" connector instead of this raw
        # row, so flag it system so list_connectors hides it (no duplicate card).
        "system": True,
    }


def datastore_config(db_path: str) -> dict[str, Any]:
    """Connector config for the read-only DuckDB demo datasource (local-file path).

    Legacy offline variant kept for ``seed.py --demo``'s local fallback.
    ``app/sample.py`` uses the parquet-backed configs above instead.
    """
    return {
        "connector_type": "duckdb",
        "database": db_path,
        "read_only": True,
        "description": "Bundled demo datasets (read-only DuckDB file).",
        # Internal: see s3_datastore_config — hidden from the connectors list so
        # the branded virtual "Demo data" connector is the only demo card.
        "system": True,
    }
